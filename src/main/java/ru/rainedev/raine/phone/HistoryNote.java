package ru.rainedev.raine.phone;

import it.tdlight.jni.TdApi;

/**
 * Приписка к сообщению истории — способ добавить к переписке то, чего в самом
 * сообщении нет.
 * <p>
 * Сама разметка сообщений менять не должна: она про то, что пришло из Telegram.
 * А вот пометить чужой пост как уже прочитанный, приписать заметку из дневника
 * или отметить, что об этом уже говорили, — это про нас, и это разное для
 * разных случаев. Приписки подключаются снаружи, поэтому их можно добавить,
 * не трогая разметку.
 */
@FunctionalInterface
public interface HistoryNote {

    /**
     * @param rendered уже размеченное сообщение
     * @return оно же, дополненное, или как было
     */
    String addTo(TdApi.Chat chat, TdApi.Message message, String rendered);
}
